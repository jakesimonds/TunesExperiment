#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const LIBRARY_DIR = path.join(__dirname, '..', 'library');
const OUTPUT_DIR = path.join(__dirname, '..', 'src', 'generated');
const MANIFEST_PATH = path.join(OUTPUT_DIR, 'music-manifest.json');
const INDEX_PATH = path.join(OUTPUT_DIR, 'library-index.ts');

const SUPPORTED_EXTENSIONS = ['.mp3', '.wav'];

function isAudioFile(filename) {
  const ext = path.extname(filename).toLowerCase();
  return SUPPORTED_EXTENSIONS.includes(ext);
}

function scanLibrary() {
  if (!fs.existsSync(LIBRARY_DIR)) {
    console.error(`Library directory not found: ${LIBRARY_DIR}`);
    process.exit(1);
  }

  const entries = fs.readdirSync(LIBRARY_DIR, { withFileTypes: true });
  const folders = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;

    const folderPath = path.join(LIBRARY_DIR, entry.name);
    const files = fs.readdirSync(folderPath)
      .filter(isAudioFile)
      .sort();

    if (files.length === 0) continue;

    const tracks = files.map((filename) => ({
      filename,
      path: `library/${entry.name}/${filename}`,
    }));

    folders.push({ name: entry.name, tracks });
  }

  folders.sort((a, b) => a.name.localeCompare(b.name));
  return { folders };
}

function writeManifest(manifest) {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  fs.writeFileSync(MANIFEST_PATH, JSON.stringify(manifest, null, 2) + '\n');
  console.log(`Wrote manifest: ${MANIFEST_PATH}`);
}

function writeLibraryIndex(manifest) {
  const lines = [
    '// Auto-generated by scripts/generate-manifest.js — do not edit',
    '',
    'const assets: Record<string, any> = {',
  ];

  for (const folder of manifest.folders) {
    for (const track of folder.tracks) {
      const key = track.path;
      const requirePath = `../../${track.path}`;
      lines.push(`  '${key}': require('${requirePath}'),`);
    }
  }

  lines.push('};');
  lines.push('');
  lines.push('export default assets;');
  lines.push('');

  fs.writeFileSync(INDEX_PATH, lines.join('\n'));
  console.log(`Wrote library index: ${INDEX_PATH}`);
}

const manifest = scanLibrary();
writeManifest(manifest);
writeLibraryIndex(manifest);

const trackCount = manifest.folders.reduce((sum, f) => sum + f.tracks.length, 0);
console.log(`Found ${manifest.folders.length} folder(s) with ${trackCount} track(s).`);
